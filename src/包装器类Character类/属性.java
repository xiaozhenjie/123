package 包装器类Character类;

public class 属性 {

	public static void main(String[] args) {
		int i1=Character.MAX_RADIX;//返回最大基数               36
		int i2=Character.MIN_RADIX;//返回最小基数               2
		char c1=Character.MAX_VALUE;//字符类型的最大值     ?
		char c2=Character.MIN_VALUE;//字符类型的最小值     空格
		System.out.println(i1);
		System.out.println(i2);
		System.out.println(c1);
		System.out.println(c2);
	}

}
