package 包装器类Character类;

public class toUpperCase方法和toLowerCase方法 {

	public static void main(String[] args) {
		//转换成大写       public static char toUpperCase(char ch)
		System.out.println("Character.toUpperCase('b'):"+Character.toUpperCase('b'));
		
		//转换成小写       public static char toLowerCase(char ch)
		System.out.println("Character.toLowerCase('Q'):"+Character.toLowerCase('Q'));
		
		System.out.println(toUpperCase方法和toLowerCase方法.class);
	}

}
